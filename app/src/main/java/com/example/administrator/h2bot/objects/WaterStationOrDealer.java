package com.example.administrator.h2bot.objects;

public class WaterStationOrDealer {
    private String station_dealer_name;
    private String userType;
    private String distance;
    private String duration;
    private String type;
    private String status;
    private String stationID;
    private double lat, lng;

    public WaterStationOrDealer(){
    }

    public String getStationID() {
        return stationID;
    }

    public void setStationID(String stationID) {
        this.stationID = stationID;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getStation_dealer_name() {
        return station_dealer_name;
    }

    public void setStation_dealer_name(String station_dealer_name) {
        this.station_dealer_name = station_dealer_name;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public String getUserType() {
        return userType;
    }

    public void setUserType(String userType) {
        this.userType = userType;
    }
}
